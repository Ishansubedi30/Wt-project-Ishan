  <h1>From Principal</h1>
  <p>BGandaki College of Engineering and Science (GCES) is a tertiary education facility in the beautiful city Pokhara. It was founded in 1998 and officially inaugurated in 1999. The college runs under the non-profitable Public Education Trust and is affiliated to Pokhara University, located inside the premises of Gandaki Boarding School (GBS), Lamachaur, Pokhara. Currently, the college offers Bachelor’s Engineering program in Software Engineering (B.E. Software) and Bachelor’s of Engineering in Computer (B.E. Computer) in Bachelor’s Level, and Master of Science in Information System Engineering (M.Sc. ISE) degree in Masters of Science (Information System Engineering) program. The college is a non-profit institution owned by the Ministry of Education—Nepal and is managed by the GBS/GCES public education trust. GCES has always been driven by the mission of becoming an international centre of academic excellence with its motto "Knowledge, Character, and Service". Our program prepares students for different areas in the field of Computer Engineering, Software Engineering, and specialized field of Information Systems Engineering. They not only prepare students to excel in their subject matters, but opens wide domain of ideas and knowledge, values, and research skills. A unique feature of our academic programs is the focus in projects and practical along with a sound theoretical background. Students utilizing this opportunity are exceptionally well prepared for industry jobs, imparting with entrepreneurial skill, research skills. GCES is refining engineering education via the integration of methods, research projects and creative learning</p></b>


